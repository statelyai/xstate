// Type-error-quality benchmark.
//
// Compiles the deliberately-broken fixtures in scripts/type-error-fixtures/
// (each contains exactly one realistic mistake) and snapshots the compiler
// output to scripts/type-error-quality.out.md. Error output is API surface:
// review the snapshot in PRs the same way you review types.
//
// Usage: node scripts/type-error-quality.mjs

import { execFileSync } from 'node:child_process';
import { writeFileSync, readdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const fixturesDir = join(root, 'scripts', 'type-error-fixtures');
const tsc = join(root, 'node_modules', '.bin', 'tsc');

let raw = '';
const start = Date.now();
try {
  raw = execFileSync(tsc, ['-p', fixturesDir, '--pretty', 'false'], {
    cwd: root,
    encoding: 'utf8'
  });
} catch (err) {
  raw = (err.stdout ?? '') + (err.stderr ?? '');
}
const elapsed = ((Date.now() - start) / 1000).toFixed(1);

const fixtures = readdirSync(fixturesDir).filter((f) => f.endsWith('.ts'));
const byFixture = Object.fromEntries(fixtures.map((f) => [f, []]));
let current = null;
for (const line of raw.split('\n')) {
  const m = line.match(/type-error-fixtures\/([\w.]+\.ts)\(/);
  if (m) current = m[1];
  else if (!/^\s/.test(line)) current = null;
  if (current && byFixture[current]) byFixture[current].push(line);
}

let out = `# Type-error-quality snapshot\n\nGenerated by \`node scripts/type-error-quality.mjs\` (tsc took ${elapsed}s).\nEach fixture contains exactly one realistic mistake; below is the complete\ncompiler output a user would see for it.\n`;
let missing = false;
for (const f of fixtures) {
  const lines = byFixture[f];
  out += `\n## ${f}\n\n`;
  if (lines.length === 0) {
    out += `**NO ERROR REPORTED — the mistake compiles cleanly. This is a soundness gap.**\n`;
    missing = true;
  } else {
    out += '```\n' + lines.join('\n') + '\n```\n';
  }
}
writeFileSync(join(root, 'scripts', 'type-error-quality.out.md'), out);
console.log(out);
console.log(`Snapshot written to scripts/type-error-quality.out.md`);
if (missing) {
  console.error('\nAt least one fixture produced no compile error.');
  process.exitCode = 1;
}
